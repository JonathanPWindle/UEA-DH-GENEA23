class AdaptiveEmbedding(nn.Module):
    def __init__(self, n_token, d_embed, d_proj, cutoffs, div_val=1, 
                 sample_softmax=False):
        super(AdaptiveEmbedding, self).__init__()

        self.n_token = n_token
        self.d_embed = d_embed

        self.cutoffs = cutoffs + [n_token]
        self.div_val = div_val
        self.d_proj = d_proj

        self.emb_scale = d_proj ** 0.5

        self.cutoff_ends = [0] + self.cutoffs

        self.emb_layers = nn.ModuleList()
        self.emb_projs = nn.ParameterList()
        if div_val == 1:
            self.emb_layers.append(
                nn.Embedding(n_token, d_embed, sparse=sample_softmax>0)
            )
            if d_proj != d_embed:
                self.emb_projs.append(nn.Parameter(torch.Tensor(d_proj, d_embed)))
        else:
            for i in range(len(self.cutoffs)):
                l_idx, r_idx = self.cutoff_ends[i], self.cutoff_ends[i+1]
                d_emb_i = d_embed // (div_val ** i)
                self.emb_layers.append(nn.Embedding(r_idx-l_idx, d_emb_i))
                self.emb_projs.append(nn.Parameter(torch.Tensor(d_proj, d_emb_i)))

    def forward(self, inp):
        if self.div_val == 1:
            embed = self.emb_layers[0](inp)
            if self.d_proj != self.d_embed:
                embed  = F.linear(embed, self.emb_projs[0])
        else:
            param = next(self.parameters())
            inp_flat = inp.view(-1)
            emb_flat = torch.zeros([inp_flat.size(0), self.d_proj], 
                dtype=param.dtype, device=param.device)
            for i in range(len(self.cutoffs)):
                l_idx, r_idx = self.cutoff_ends[i], self.cutoff_ends[i + 1]

                mask_i = (inp_flat >= l_idx) & (inp_flat < r_idx)
                indices_i = mask_i.nonzero().squeeze()

                if indices_i.numel() == 0:
                    continue

                inp_i = inp_flat.index_select(0, indices_i) - l_idx
                emb_i = self.emb_layers[i](inp_i)
                emb_i = F.linear(emb_i, self.emb_projs[i])

                emb_flat.index_copy_(0, indices_i, emb_i)

            embed = emb_flat.view(*inp.size(), self.d_proj)

        embed.mul_(self.emb_scale)

        return embed


class TimestepEmbedder(nn.Module):
    def __init__(self, latent_dim, sequence_pos_encoder):
        super().__init__()
        self.latent_dim = latent_dim
        self.sequence_pos_encoder = sequence_pos_encoder

        time_embed_dim = self.latent_dim
        self.time_embed = nn.Sequential(
            nn.Linear(self.latent_dim, time_embed_dim),
            nn.SiLU(),
            nn.Linear(time_embed_dim, time_embed_dim),
        )

    def forward(self, timesteps):
        return self.time_embed(self.sequence_pos_encoder.pe[timesteps])

class CrossAttentiveTransformerXL(nn.Module):
    def __init__(self, n_encoder_layer, n_decoder_layer, n_head, d_model, d_head, d_inner, dropout, dropatt, d_embed=None, pre_lnorm=False, tgt_len=None, ext_len=None, mem_len=None, same_length=False, attn_type=0, clamp_len=-1, sample_softmax=-1, pose_size=453, pose_embedding_size=256, num_speaker=17, speaker_emb_size=2, text_embedding_size=300, audio_embedding_size=768, **kwargs):
        super(CrossAttentiveTransformerXL, self).__init__()
        self.model_type = 'CrossAttentiveTransformerXL'

        self.feature_size = audio_embedding_size + text_embedding_size + speaker_emb_size
        self.seq_pos_enc = PositionalEncoding(self.feature_size, 0.2, 1000)
        self.time_mlp = TimestepEmbedder(self.feature_size, self.seq_pos_enc)
        self.pose_emb = nn.Linear(pose_size, pose_embedding_size)
        self.speaker_embedding = torch.nn.Embedding(num_speaker, speaker_emb_size)
        
        d_embed = d_model if d_embed is None else d_embed
        self.d_embed = d_embed
        self.d_model = d_model
        self.n_head = n_head
        self.d_head = d_head
        
        self.decoder = nn.Linear(d_embed, pose_size)

        self.feature_embedding = nn.Linear(self.feature_size, d_embed)


        self.drop = nn.Dropout(dropout)

        self.n_encoder_layer = n_encoder_layer
        self.n_decoder_layer = n_decoder_layer


        self.tgt_len = tgt_len
        self.mem_len = mem_len
        self.ext_len = ext_len
        self.max_klen = tgt_len + ext_len + mem_len

        self.attn_type = attn_type

        self.encoder_layers = nn.ModuleList()
        for i in range(n_encoder_layer):
            self.encoder_layers.append(
                RelPartialLearnableDecoderLayer(
                    n_head, d_model, d_head, d_inner, dropout,
                    tgt_len=tgt_len, ext_len=ext_len, mem_len=mem_len,
                    dropatt=dropatt, pre_lnorm=pre_lnorm)
            )
        self.decoder_layers = nn.ModuleList()
        for i in range(n_decoder_layer):
            self.decoder_layers.append(
                RelPartialLearnableCrossDecoderLayer(
                    n_head, d_model, d_head, d_inner, dropout,
                    tgt_len=tgt_len, ext_len=ext_len, mem_len=mem_len,
                    dropatt=dropatt, pre_lnorm=pre_lnorm)
            )

        self.sample_softmax = sample_softmax
        self.same_length = same_length
        self.clamp_len = clamp_len

        self._create_params()

    def get_emotion_vector(self, emotion):
        emotion_emb = self.emotion_embedding(emotion)
        emotion_emb = torch.squeeze(emotion_emb)
        return emotion_emb

    def backward_compatible(self):
        self.sample_softmax = -1

    def _create_params(self):
        if self.attn_type == 0: # default attention
            self.pos_emb = PositionalEmbedding(self.d_model)
            self.r_w_bias = nn.Parameter(torch.Tensor(self.n_head, self.d_head))
            self.r_r_bias = nn.Parameter(torch.Tensor(self.n_head, self.d_head))
        elif self.attn_type == 1: # learnable
            self.r_emb = nn.Parameter(torch.Tensor(
                    self.n_layer, self.max_klen, self.n_head, self.d_head))
            self.r_w_bias = nn.Parameter(torch.Tensor(
                    self.n_layer, self.n_head, self.d_head))
            self.r_bias = nn.Parameter(torch.Tensor(
                    self.n_layer, self.max_klen, self.n_head))
        elif self.attn_type == 2: # absolute standard
            self.pos_emb = PositionalEmbedding(self.d_model)
        elif self.attn_type == 3: # absolute deeper SA
            self.r_emb = nn.Parameter(torch.Tensor(
                    self.n_layer, self.max_klen, self.n_head, self.d_head))

    def reset_length(self, tgt_len, ext_len, mem_len):
        self.tgt_len = tgt_len
        self.mem_len = mem_len
        self.ext_len = ext_len

    def init_mems(self):
        if self.mem_len > 0:
            mems = []
            param = next(self.parameters())
            for i in range(self.n_encoder_layer + self.n_decoder_layer+1):
                empty = torch.empty(0, dtype=param.dtype, device=param.device)
                mems.append(empty)

            return mems
        else:
            return None

    def _update_mems(self, hids, mems, qlen, mlen):
        # does not deal with None
        if mems is None: return None

        # mems is not None
        assert len(hids) == len(mems), 'len(hids) != len(mems)'

        # There are `mlen + qlen` steps that can be cached into mems
        # For the next step, the last `ext_len` of the `qlen` tokens
        # will be used as the extended context. Hence, we only cache
        # the tokens from `mlen + qlen - self.ext_len - self.mem_len`
        # to `mlen + qlen - self.ext_len`.
        with torch.no_grad():
            new_mems = []
            end_idx = mlen + max(0, qlen - 0 - self.ext_len)
            beg_idx = max(0, end_idx - self.mem_len)
            for i in range(len(hids)):

                cat = torch.cat([mems[i], hids[i]], dim=0)
                new_mems.append(cat[beg_idx:end_idx].detach())

        return new_mems

    def forward(self, audio, text, speaker_id, inter_audio, inter_text, inter_speaker_id, mems, speaker_emb=None):
        if not mems: mems = self.init_mems()

        if speaker_emb is None:
            speaker_emb = self.speaker_embedding(speaker_id)
            speaker_emb = torch.squeeze(speaker_emb)
            inter_speaker_emb = self.speaker_embedding(inter_speaker_id)
            inter_speaker_emb = torch.squeeze(inter_speaker_emb)

        if speaker_emb.ndim == 2:
            speaker_emb = speaker_emb.unsqueeze(0)
            inter_speaker_emb = inter_speaker_emb.unsqueeze(0)


        features = torch.cat((audio, text), axis=-1)
        
        features = torch.cat((speaker_emb, features), axis=-1)

        features = self.feature_embedding(features).permute(1,0,2)

        inter_features = torch.cat((inter_audio, inter_text), axis=-1)
        
        inter_features = torch.cat((inter_speaker_emb, inter_features), axis=-1)

        inter_features = self.feature_embedding(inter_features).permute(1,0,2)

        qlen, bs, pose_size = features.size()

        mlen = mems[0].size(0) if mems is not None else 0
        klen = mlen + qlen

        all_ones = features.new_ones(qlen, klen)
        mask_len = klen - self.mem_len
        if mask_len > 0:
            mask_shift_len = qlen - mask_len
        else:
            mask_shift_len = qlen
        dec_attn_mask = (torch.triu(all_ones, 1+mlen)
                + torch.tril(all_ones, -mask_shift_len)).byte()[:, :, None] # -1
        
        dec_attn_mask = dec_attn_mask > 0
    
        
        
        hids = []
        pos_seq = torch.arange(klen-1, -1, -1.0, device=features.device, 
                                dtype=features.dtype)
        if self.clamp_len > 0:
            pos_seq.clamp_(max=self.clamp_len)
        pos_emb = self.pos_emb(pos_seq)

        core_out = self.drop(features)
        pos_emb = self.drop(pos_emb)
        inter_cross = self.drop(inter_features)


        hids.append(core_out)
        for i, layer in enumerate(self.encoder_layers):
            mems_i = None if mems is None else mems[i]
            core_out = layer(core_out, pos_emb, self.r_w_bias,
                    self.r_r_bias, dec_attn_mask=dec_attn_mask, mems=mems_i)
            hids.append(core_out)
        
        enc_out = core_out

        core_out = inter_cross
        
        for i, layer in enumerate(self.decoder_layers):
            mems_i = None if mems is None else mems[i+self.n_encoder_layer]
            core_out = layer(enc_out, core_out, pos_emb, self.r_w_bias,
                    self.r_r_bias, dec_attn_mask=dec_attn_mask, mems=mems_i)
            hids.append(core_out)

        core_out = self.drop(core_out)
        core_out = core_out.permute(1,0,2)
        pred_hid = core_out[:, -(qlen):, :]
        core_out = self.decoder(pred_hid)
        new_mems = self._update_mems(hids, mems, mlen, qlen)

        return core_out, new_mems

